import { Head, Link, useForm } from '@inertiajs/react';
import GuestLayout from '@/Layouts/GuestLayout';
import InputError from '@/Components/InputError';
import InputLabel from '@/Components/InputLabel';
import PrimaryButton from '@/Components/PrimaryButton';
import TextInput from '@/Components/TextInput';

const ROLES = [
    { value: 'etudiant',   label: 'Etudiant' },
    { value: 'professeur', label: 'Professeur' },
    { value: 'autre',      label: 'Autre (auto-challenge)' },
];

export default function Register() {
    const { data, setData, post, processing, errors, reset } = useForm({
        name:                  '',
        email:                 '',
        password:              '',
        password_confirmation: '',
        role:                  '',
    });

    const submit = (e) => {
        e.preventDefault();
        post(route('register'), {
            onFinish: () => reset('password', 'password_confirmation'),
        });
    };

    return (
        <GuestLayout>
            <Head title="Inscription" />

            <form onSubmit={submit} className="space-y-5">
                {/* Nom */}
                <div>
                    <InputLabel htmlFor="name" value="Nom complet" />
                    <TextInput
                        id="name"
                        name="name"
                        value={data.name}
                        className="mt-1 block w-full"
                        autoComplete="name"
                        isFocused
                        onChange={(e) => setData('name', e.target.value)}
                        required
                    />
                    <InputError message={errors.name} className="mt-2" />
                </div>

                {/* Email */}
                <div>
                    <InputLabel htmlFor="email" value="Adresse email" />
                    <TextInput
                        id="email"
                        type="email"
                        name="email"
                        value={data.email}
                        className="mt-1 block w-full"
                        autoComplete="username"
                        onChange={(e) => setData('email', e.target.value)}
                        required
                    />
                    <InputError message={errors.email} className="mt-2" />
                </div>

                {/* Rôle — champ obligatoire */}
                <div>
                    <InputLabel htmlFor="role" value="Votre role" />
                    <select
                        id="role"
                        name="role"
                        value={data.role}
                        onChange={(e) => setData('role', e.target.value)}
                        required
                        className="mt-1 block w-full rounded-xl border border-gray-200
                                   bg-white px-4 py-3 text-sm text-gray-900
                                   focus:border-indigo-500 focus:ring-2 focus:ring-indigo-500
                                   dark:border-gray-700 dark:bg-gray-900 dark:text-gray-100"
                    >
                        <option value="" disabled>Selectionnez votre role</option>
                        {ROLES.map((r) => (
                            <option key={r.value} value={r.value}>
                                {r.label}
                            </option>
                        ))}
                    </select>
                    <InputError message={errors.role} className="mt-2" />
                </div>

                {/* Mot de passe */}
                <div>
                    <InputLabel htmlFor="password" value="Mot de passe" />
                    <TextInput
                        id="password"
                        type="password"
                        name="password"
                        value={data.password}
                        className="mt-1 block w-full"
                        autoComplete="new-password"
                        onChange={(e) => setData('password', e.target.value)}
                        required
                    />
                    <InputError message={errors.password} className="mt-2" />
                </div>

                {/* Confirmation mot de passe */}
                <div>
                    <InputLabel htmlFor="password_confirmation" value="Confirmer le mot de passe" />
                    <TextInput
                        id="password_confirmation"
                        type="password"
                        name="password_confirmation"
                        value={data.password_confirmation}
                        className="mt-1 block w-full"
                        autoComplete="new-password"
                        onChange={(e) => setData('password_confirmation', e.target.value)}
                        required
                    />
                    <InputError message={errors.password_confirmation} className="mt-2" />
                </div>

                <div className="flex items-center justify-between pt-2">
                    <Link
                        href={route('login')}
                        className="text-sm text-gray-500 underline hover:text-gray-700
                                   dark:text-gray-400 dark:hover:text-gray-200"
                    >
                        Deja inscrit ?
                    </Link>

                    <PrimaryButton
                        className="ml-4 bg-indigo-600 hover:bg-indigo-500"
                        disabled={processing}
                    >
                        {processing ? 'Inscription...' : "S'inscrire"}
                    </PrimaryButton>
                </div>
            </form>
        </GuestLayout>
    );
}
